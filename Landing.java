    private void CodePinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CodePinActionPerformed
        // Mengecek apakah input adalah angka
        boolean isInteger = evt.getActionCommand().matches("^\\d.*$");
        
       
        if (!isInteger) {
            // Menampilkan pesan jika input bukan angka
            JOptionPane.showMessageDialog(this, "PIN harus berupa angka.");
        } else {
            // Jika input valid (angka), lanjutkan proses
            int maxLength = evt.getActionCommand().length();
            
            if (maxLength > 6) {
                // Memastikan panjang input tidak melebihi 6 karakter
                JOptionPane.showMessageDialog(this, "Jumlah maksimal PIN adalah 6 karakter.");
              // Memeriksa apakah PIN yang dimasukkan sesuai dengan nilai tertentu
            } else if (evt.getActionCommand().matches("123456")) {
                JOptionPane.showMessageDialog(this, "PIN Benar");
                // Menampilkan menu utama dan menutup jendela saat ini
                new Menu_Utama().setVisible(true);
                this.setVisible(false);
            } else {
                // Menampilkan pesan kesalahan jika PIN tidak cocok
                JOptionPane.showMessageDialog(this, "PIN Salah");
            }
        }
    }//GEN-LAST:event_CodePinActionPerformed

